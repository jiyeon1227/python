import math
# from 형태로 import
# from math import factorial

# from 형태로 import 하면 앞에 math. 을 안붙여도됨
from math import factorial
print('5!=',factorial(5))

# 원주율(pi) 구하기
print(math.pi)

# 2의 3승 구하기
print('2의 3승=', math.pow(2, 3))

# 팩토리얼(factorial) 구하기
print('3!=',math.factorial(3))
print(math.factorial(984))

# ceil()함수 : 올림기능
print(math.ceil(3.1))

# floor(): 내림기능
print(math.floor(3.9))

# sqrt() : 제곱근
print(math.sqrt(5))